package javajdbc;

public class Employee {

	int id;
	String name;
	int pho;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPho() {
		return pho;
	}
	public void setPho(int pho) {
		this.pho = pho;
	}
}
